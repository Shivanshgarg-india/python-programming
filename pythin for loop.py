#For loop using break
for letter in 'Shiavsnh':

    if letter == 'i' or letter == 'n':
        break

print('Current Letter :', letter)


# for loop using continue
for letter in 'Shivansh':
    if letter == 'i' or letter == 'n':
        continue
    print('Current Letter :', letter)


